package com.goit.lessons.homeWork.module04.distanceBetweenPoints;


public class SecondCoordinate {

    private double x;
    private double y;

    SecondCoordinate(double x, double y){
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
