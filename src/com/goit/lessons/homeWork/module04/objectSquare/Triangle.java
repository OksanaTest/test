package com.goit.lessons.homeWork.module04.objectSquare;


public class Triangle {

    private int h;
    private int a;

    Triangle(int h, int a){
        this.h = h;
        this.a = a;
    };

    public int getH() {
        return this.h;
    };

    public int getA() {
        return this.a;
    };

}
